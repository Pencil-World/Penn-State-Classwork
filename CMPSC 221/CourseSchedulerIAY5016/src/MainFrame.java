    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }

    public void rebuildCourseCodeComboBoxes() {
        ArrayList<String> courseCodes = CourseQueries.getAllCourseCodes();
        addCourseCodeCombobox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        if (courseCodes.size() > 0) {
            addCourseCodeCombobox.setSelectedItem(courseCodes.get(0));
        }
    }

    public void rebuildClassComboBoxes() {
        String target_semester = (String) currentSemesterComboBox.getSelectedItem();
        ArrayList<String> classes = ClassQueries.getAllCourseCodes(target_semester);
        scheduleClassCombobox.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        dropCourseCodeCombobox.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        selectCourseCombobox.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        dropCourseCombobox.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        if (classes.size() > 0) {
            scheduleClassCombobox.setSelectedItem(classes.get(0));
            dropCourseCodeCombobox.setSelectedItem(classes.get(0));
            selectCourseCombobox.setSelectedItem(classes.get(0));
            dropCourseCombobox.setSelectedItem(classes.get(0));
        }
    }

    public void rebuildStudentComboBoxes() {
        ArrayList<String> students = new ArrayList<>();
        for (StudentEntry studentEntry: StudentQueries.getAllStudents())
            students.add(studentEntry.getLastName() + ", " + studentEntry.getFirstName());
        scheduleStudentCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        displayStudentCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        dropStudentCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        dropStudentSelectCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        if (students.size() > 0) {
            scheduleStudentCombobox.setSelectedItem(students.get(0));
            displayStudentCombobox.setSelectedItem(students.get(0));
            dropStudentCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
            dropStudentSelectCombobox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        }
    }


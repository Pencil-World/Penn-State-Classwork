    private void dropClassDropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropClassDropButtonActionPerformed
        StudentEntry student=StudentQueries.getAllStudents().get(dropStudentSelectCombobox.getSelectedIndex());
        String course=dropCourseCombobox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> waitingStudents=ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, course);
        ArrayList<StudentEntry> scheduled=MultiTableQueries.getScheduledStudentsByClass(currentSemester, course);
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), course);
        dropCourseTextArea.setText(dropCourseCombobox.getSelectedItem().toString() + " has been dropped from " + course);
        if(scheduled.contains(student)){
            ScheduleQueries.updateScheduleEntry(currentSemester, waitingStudents.get(0));
            StudentEntry s=StudentQueries.getStudent(waitingStudents.get(0).getStudentID());
            dropCourseTextArea.append("\n" + s.getLastName() + ", " + s.getFirstName() +  " has been scheduled into " + course);
        }
        rebuildCourseCodeComboBoxes();
        rebuildClassComboBoxes();
    }//GEN-LAST:event_dropClassDropButtonActionPerformed

    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            project = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            project = "Course Scheduler Spring 2024";

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(project.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }


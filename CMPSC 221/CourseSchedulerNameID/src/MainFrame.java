    private void ScheduleClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleClassButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = scheduleCourseComboBox.getSelectedItem().toString();
        ArrayList<ClassDescription> courses = MultiTableQueries.getAllClassDescriptions(currentSemester);
        ClassDescription course = null;
        for (ClassDescription c: courses){
            if (c.getCourseCode().equals(courseCode)){
                course = c;
            }
        }
        StudentEntry student = getStudentFromComboBox(scheduleStudentComboBox.getSelectedIndex());
        
        Long datetime = System.currentTimeMillis();
        Timestamp timestamp = new Timestamp(datetime);
//        Current Time Stamp
        ScheduleEntry sch;
        int seatsTaken = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        if(seatsTaken < course.getSeats()){
            sch = new ScheduleEntry(currentSemester, courseCode, student.getStudentID(), "S", timestamp);
            scheduleStudentStatusLabel.setText(student.getLastName() + ", " + student.getFirstName() + " has been Scheduled for " + courseCode);
        } else {
            sch = new ScheduleEntry(currentSemester, courseCode, student.getStudentID(), "W", timestamp);
            scheduleStudentStatusLabel.setText(student.getLastName() + ", " + student.getFirstName() + " has been Waitlisted for " + courseCode);
        }
        ScheduleQueries.addScheduleEntry(sch);
        rebuildCourseComboBox();
        rebuildStudentComboBox();

    }//GEN-LAST:event_ScheduleClassButtonActionPerformed


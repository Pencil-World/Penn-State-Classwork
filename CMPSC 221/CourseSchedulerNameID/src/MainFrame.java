    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    public void rebuildCourseComboBox(){
        scheduleCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        selectCourseCodeComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        dropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        dropClassAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        ArrayList<String> courses = ClassQueries.getAllCourseCodes(currentSemester);
        for(String x: courses){
            scheduleCourseComboBox.addItem(x);
            selectCourseCodeComboBox.addItem(x);
            dropCourseComboBox.addItem(x);
            dropClassAdminComboBox.addItem(x);
        }
    }
    
    public void rebuildStudentComboBox(){
        scheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        dropStudentAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        dropCourseStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        for(StudentEntry x: students){
            scheduleStudentComboBox.addItem(x.getLastName() + ", " + x.getFirstName());
            selectStudentComboBox.addItem(x.getLastName() + ", " + x.getFirstName());
            dropStudentAdminComboBox.addItem(x.getLastName() + ", " + x.getFirstName());
            dropCourseStudentComboBox.addItem(x.getLastName() + ", " + x.getFirstName());
            
        }
    }
    private StudentEntry getStudentFromComboBox(int index){
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
//        int index = scheduleCourseStudentComboBox.getSelectedIndex();
        int x = 0;
        StudentEntry student = null;
        for (StudentEntry s: students){
            if (index == x){
                student = s;
                break;
            } else {
                x+=1;
            }
        }
        return student;
    }

    private void DropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropCourseButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student=getStudentFromComboBox(dropCourseStudentComboBox.getSelectedIndex());
        String course=dropCourseComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> waitingStudents=ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, course);
        ArrayList<StudentEntry> scheduled=MultiTableQueries.getScheduledStudentsByClass(currentSemester, course);
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), course);
        dropCourseStudentTextArea.setText(dropCourseStudentComboBox.getSelectedItem().toString() + " has been dropped from " + course);
        if(scheduled.contains(student)){
            ScheduleQueries.updateScheduleEntry(currentSemester, waitingStudents.get(0));
            StudentEntry s=StudentQueries.getStudent(waitingStudents.get(0).getStudentID());
            dropCourseStudentTextArea.append("\n" + s.getLastName() + ", " + s.getFirstName() +  " has been scheduled into " + course);
        }
        rebuildSemesterComboBoxes();
        rebuildCourseComboBox();
        rebuildStudentComboBox();
    }//GEN-LAST:event_DropCourseButtonActionPerformed

    private void dropClassAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropClassAdminButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = dropCourseComboBox.getSelectedItem().toString();
        ArrayList<StudentEntry> scheduledStudentsSchedules =  MultiTableQueries.getScheduledStudentsByClass(currentSemester, courseCode);
        ArrayList<StudentEntry> waitlistedStudentsSchedules = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
        dropClassAdminTextArea.setText("Scheduled Students dropped from the course: ");
        for(StudentEntry student: scheduledStudentsSchedules){
            dropClassAdminTextArea.append("\n"+ student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID());
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), courseCode);
        }
        dropClassAdminTextArea.append("\n\nWaitlisted Students dropped from the course: ");
        for(StudentEntry student: waitlistedStudentsSchedules){    
            dropClassAdminTextArea.append("\n"+ student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID());
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), courseCode);
        }
        ClassQueries.dropClass(currentSemester, courseCode);
        rebuildSemesterComboBoxes();
        rebuildCourseComboBox();
        rebuildStudentComboBox();

    }//GEN-LAST:event_dropClassAdminButtonActionPerformed

    private void dropStudentAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentAdminButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = getStudentFromComboBox(dropStudentAdminComboBox.getSelectedIndex());
        dropStudentAdminTextArea.setText(dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from the list of students.");
        StudentQueries.dropStudent(student.getStudentID());
        ArrayList<String> semesters = SemesterQueries.getSemesterList();

        for (String semester: semesters){
            dropStudentAdminTextArea.append("\n\nFor Semester: " + semester);
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, student.getStudentID());
            for (ScheduleEntry schedule: schedules){
                if(schedule.getStatus().equals("S")){
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentAdminTextArea.append("\n" + dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from " + schedule.getCourseCode());
                    ScheduleEntry waitlist = ScheduleQueries.getWaitlistedStudentsByClass(semester, schedule.getCourseCode()).get(0);
                    if (waitlist != null){
                        StudentEntry studentOff = StudentQueries.getStudent(waitlist.getStudentID());
                        dropStudentAdminTextArea.append("\n" + studentOff.getLastName() + ", " + studentOff.getFirstName() +  " has been scheduled into " + schedule.getCourseCode());
                        ScheduleQueries.updateScheduleEntry(semester, waitlist);
                    }
                } else {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentAdminTextArea.append("\n" + dropStudentAdminComboBox.getSelectedItem().toString() + " has been dropped from the waitlist for " + schedule.getCourseCode());
                }
            }
        }
    }//GEN-LAST:event_dropStudentAdminButtonActionPerformed

